                                                            اهلل عزوجل الطالق )رواه أبو داود وابن ماجه
                                                        </p>
                                                    </ol>
                                                    <p class="content-list-level-cir">
                                                        ” Nabi saw bersabda : Perbuatan halal yang paling dibenci Allah
                                                        adalah talak”. (HR Abu Dawud dan Ibn Majah)
