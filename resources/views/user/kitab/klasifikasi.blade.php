                        <h2 id="judul" class="text-capitalize my-2">Klasifikasi Hadits</h2>
                        <p class="content">
                            Hadits dapat diklasifikasikan berdasarkan beberapa kriteria yakni berdasarkan asal ujung
                            sanad, keutuhan rantai sanad, banyak periwayat serta tingkat keaslian hadits (dapat diterima
                            atau tidaknya hadits bersangkutan). Oleh karena itulah, kita juga harus mengetahui
                            macam-macam hadist. Hal ini lantaran banyaknya hadist yang berasal dari sumber yang rancu
                            maupun kalangan palsu. Supaya menambah pengetahuan kita mengenai macam-macam hadist, berikut
                            ini penjelasan lengkap macam-macam hadist sesuai klasifikasinya.
                        </p>
                        <div class="my-4 px-5 py-1 text-nowrap text-start subtitle">
                            <h3 class="m-0 py-1">Berdasarkan Kuantitas Sanad</h3>
                        </div>
                        <ol>
                            <li class="content-list-level-ul fw-bold">
                                Hadits Mutawatir
                                <ol>
                                    <li class="content-list-level-dec">
                                        Pengertian
                                        <p class="content">
                                            Dari segi bahasa kata mutawatir berasal dari kata ”Tawaatur” yang berarti
                                            datangnya satu setelah satu dengan adanya jarak antara keduanya, atau
                                            ”at-tatabu’”yang berarti beriring-iringan atau berturut-turut antara satu
                                            dengan yang lain.</p>

                                        <p class="content">Hadits mutawatir dari segi terminologis mempunyai banyak
                                            definisi. Menurut
                                            Nurudin Nurudin Itr hadits mutawatir adalah:</p>

                                        <p class="content">“Hadits yang diriwayatkan oleh sejumlah rawi yang tidak
                                            mungkin bersepakat
                                            untuk berdusta dari sejumlah rawi yang semisal mereka dan seterusnya sampai
                                            akhir sanad dan semuanya bersandar kepada panca indera.”</p>
                                        <p class="content">Sedang menurut Ajjaj al Khatib, hadits mutawatir adalah:</p>
                                        <p class="content">“Hadits yg diriwayatkan oleh sebagian besar perawi yang
                                            menurut adat mereka
                                            tidak mungkin bersepakat dusta dari masing-masing mereka mulai dari awal
                                            sanad sampai dari masing-masing mereka mulai dari awal sanad sampai akhir
                                            sanad.”
                                        </p>
                                    </li>
                                    <li class="content-list-level-dec">
                                        Syarat
                                        <p class="content">
                                            Suatu hadits dapat dikatakan mutawatir apabila telah memenuhi persyaratan
                                            sebagai berikut:</p>
                                        <ol>
                                            <li class="content-list-level-cir">
                                                Hadits yang diberitakan oleh rawi-rawi tersebut harus berdasarkan
                                                tanggapan
                                                (daya tangkap) pancaindera. Artinya bahwa berita yang disampaikan itu
                                                benar-benar merupakan hasil pendengaran atau penglihatan perowi. Apabila
                                                berita itu merupakan hasil pemikiran semata atau rangkuman dari
                                                peristiwa-peristiwa yang lain dan yang semacamnya, dalam arti tidak
                                                merupakan hasil tanggapan pancaindera (tidak didengar atau dilihat)
                                                sendiri
                                                oleh pemberitanya, maka tidak dapat disebut hadits mutawatir walaupun
                                                rawi
                                                yang memberikan itu mencapai jumlah yang banyak.
                                            </li>
                                            <li class="content-list-level-cir">
                                                <p class="content">Diriwayatkan oleh perowi yang banyak. Bilangan para
                                                    perawi mencapai
                                                    suatu
                                                    jumlah yang menurut adat mustahil mereka untuk berdusta. Dalam hal
                                                    ini
                                                    para
                                                    ulama berbeda pendapat tentang batasan jumlah untuk tidak
                                                    memungkinkan
                                                    bersepakat dusta. Perbedaan itu adalah dapat dijelaskan sebagai
                                                    berikut:</p>
                                                <ol>
                                                    <li class="content">
                                                        Abu Thayib menentukan sekurang-kurangnya 4 orang. Hal tersebut
                                                        diqiyaskan
                                                        dengan jumlah saksi yang diperlukan oleh hakim.
                                                    </li>
                                                    <li class="content">
                                                        Ashabus Syafi’i menentukan minimal 5 orang. Hal tersebut
                                                        diqiyaskan dengan
                                                        jumlah para Nabi yang mendapatkan gelar Ulul Azmi.
                                                    </li>
                                                    <li class="content">
                                                        Sebagian ulama menetapkan sekurang-kurangnya 20 orang. Hal
                                                        tersebut
                                                        berdasarkan ketentuan yang telah difirmankan Allah tentang
                                                        orang-orang
                                                        mukmin yang tahan uji, yang dapat mengalahkan orang-orang kafir
                                                        sejumlah 200
                                                        orang (lihat surat Al-Anfal ayat 65).
                                                    </li>
                                                    <li class="content">
                                                        Ulama yang lain menetapkan jumlah tersebut sekurang-kurangnya 40
                                                        orang. Hal
                                                        tersebut diqiyaskan dengan firman Allah QS. Al-Anfal: 6.
                                                    </li>
                                                </ol>
                                            </li>
                                            <li class="content-list-level-cir">
                                                Seimbang jumlah para perawi, sejak dalam thabaqat (lapisan/ tingkatan)
                                                pertama maupun thabaqat berikutnya.
                                            </li>
                                        </ol>
                                    </li>
                                    <li class="content-list-level-dec">
                                        Faedah
                                        <p class="content">
                                            Hadits mutawatir memberikan faedah ilmu daruri, yakni keharusan untuk
                                            menerimanya secara bulat sesuatu yang diberitahukan mutawatir karena ia
                                            membawa keyakinan yang qath’i (pasti), dengan seyakin-yakinnya bahwa Nabi
                                            Muhammad SAW benar-benar menyabdakan atau mengerjakan sesuatu seperti yang
                                            diriwayatkan oleh rawi-rawi mutawatir.
                                        </p>
                                        <p class="content">
                                            Dengan demikian, dapatlah dikatakan bahwa penelitian terhadap rawi-rawi
                                            hadits mutawatir tentang keadilan dan kedlabitannya tidak diperlukan lagi,
                                            karena kuantitas/jumlah rawi-rawinya mencapai ketentuan yang dapat menjamin
                                            untuk tidak bersepakat dusta.
                                        </p>
                                        <p class="content">
                                            Oleh karenanya wajiblah bagi setiap muslim menerima dan mengamalkan semua
                                            hadits mutawatir. Umat Islam telah sepakat tentang faedah hadits mutawatir
                                            seperti tersebut di atas dan bahkan orang yang mengingkari hasil ilmu daruri
                                            dari hadits mutawatir sama halnya dengan mengingkari hasil ilmu daruri yang
                                            berdasarkan musyahailat (penglibatan pancaindera).
                                        </p>
                                    </li>
                                    <li class="content-list-level-dec">
                                        Macam-macam
                                        <p class="content">Para ulama membagi hadis mutawatir menjadi 3 macam, yaitu :
                                        </p>
                                        <ol>
                                            <li class="content-list-level-ll">
                                                Mutawatir Lafdhi
                                                <p class="content" contenteditable="true">
                                                    Hadits mutawatir lafdhi adalah hadits yang mutawatir lafadz dan
                                                    maknanya.
                                                </p>
                                                <p class="content" contenteditable="true">
                                                    Contoh hadits mutawatir lafdzi adalah sebagaimana hadits dari Abu
                                                    Hurairah dari Rasulullah SAW yang berbunyi:
                                                </p>
                                                <p class="content-arab" contenteditable="true">
                                                    من كذب علي متعمدا فليتبوأ مقعده من النار. رواه البخارى
                                                </p>
                                                <p class="content fst-italic" contenteditable="true">
                                                    “Barang siapa yang sengaja bedusta atas namaku maka hendaklah
                                                    tempatnya di neraka.” (HR. Bukhari)
                                                </p>
                                            </li>
                                            <li class="content-list-level-ll">
                                                Mutawatir Maknawi
                                                <p class="content">
                                                    Yang dimaksud dengan hadits mutawatir maknawi adalah hadits yang
                                                    berlainan bunyi
                                                    dan maknanya, tetapi dapat diambil makna yang umum.
                                                </p>
                                                <p class="content">
                                                    Jadi hadis mutawatir maknawi adalah hadis mutawatir yang para
                                                    perawinya berbeda
                                                    dalam menyusun redaksi hadis tersebut, namun terdapat persesuaian
                                                    atau kesamaan
                                                    dalam maknanya. Contoh hadits mutawatir maknawi adalah hadits yang
                                                    menerangkan
                                                    tentang kedudukan niat dalam perbuatan.
                                                </p>
                                                <p class="content">
                                                    Hadits-hadits semacam ini banyak sekali meskipun terdapat dalam
                                                    berbagai kasus.
                                                    Contoh lain hadis Nabi saw yang berbunyi:
                                                </p>
                                                <p class="content-arab">
                                                    ما رفع صل الله عليه وسلم يديه حتي رؤي بياض إبطيه يف شيئ من دعائه إلا
                                                    في
                                                    الإستسقاء ) متفق عليه
                                                </p>
                                                <p class="content fst-italic">
                                                    “Rasulullah SAW tidak mengangkat kedua tangan beliau dalam
                                                    doa-doanya selain
                                                    dalam doa salat istiqa’ dan beliau mengangkat tangannya, sehingga
                                                    nampak
                                                    putih-putih kedua ketiaknya.” (HR. Bukhari Muslim)
                                                </p>
                                                <p class="content">
                                                    Hadis yang semakna dengan hadis tersebut di atas ada banyak, yaitu
                                                    tidak kurang
                                                    dari 30 buah dengan redaksi yang berbeda-beda. Antara lain
                                                    hadis-hadis yang
                                                    ditakhrijkan oleh Imam ahmad, Al-Hakim dan Abu Daud yang berbunyi :
                                                </p>
                                                <p class="content-arab">
                                                    كان يرفع يديه حذو منكبيه
                                                </p>
                                                <p class="content fst-italic">
                                                    ”Rasulullah SAW mengangkat tangan sejajar dengan kedua pundak
                                                    beliau.”
                                                </p>
                                            </li>
                                            <li class="content-list-level-ll">
                                                Mutawatir ‘Amali
                                                <p class="content">
                                                    Hadits mutawatir ‘amali adalah sesuatu yang diketahui dengan mudah
                                                    bahwa ia dari
                                                    agama dan telah mutawatir di kalangan umat Islam. Bahwa Nabi SAW
                                                    mengajarkannya
                                                    atau menyuruhnya atas selain dari itu. Dari hal itu dapat dikatakan
                                                    soal yang
                                                    telah disepakati.
                                                </p>
                                                <p class="content">
                                                    Contoh: berita-berita yang menerangkan waktu raka’at shalat, shalat
                                                    jenazah,
                                                    shalat ied, hijab perempuan yang bukan mahram, kadar zakat, dan
                                                    segala rupa amal
                                                    yang telah menjadi kesepakatan ijma’.
                                                </p>
                                            </li>
                                        </ol>
                                    </li>
                                </ol>
