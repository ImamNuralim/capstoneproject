                                                Hadits Masyhur
                                                <p class="content">
                                                    Menurut masyhur berasal dari kata شهر yang berarti اعلن yang berarti
                                                    mengumumkan.
                                                </p>

                                                <p class="content"> Secara terminology hadits masyhur adalah:</p>

                                                <p class="content-arab"> ما رواه ثالثة فاكرثولم يصل درجة اتلواتر
                                                </p>
                                                <p class="content"> “Hadits yang diriwayatkan oleh tiga orang atau lebih
                                                    (dalam suatu
                                                    thabaqahnya) namun belum mencapai derajat mutawatir”.
                                                </p>
                                                <p class="content">
                                                    Kemasyhuran sebuah hadis tidak mesti mencakup semua kalangan ulama.
                                                    Hadis dapat dapat saja masyhur di kalangan ulama tertentu, dalam hal
                                                    ini hadis masyhur dibedakan minimal menjadi empat macam :
                                                </p>
                                                <ol class="nk">
