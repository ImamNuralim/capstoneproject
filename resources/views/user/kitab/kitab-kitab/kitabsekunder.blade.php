                        <p class="content">
                            Istilah kitab hadis sekunder tampaknya masih merupakan istilah di komunitas terbatas
                            terutama para pengkaji hadis di Indonesia. Dalam komunitas ini, kata sekunder yang menjadi
                            kata sifat dari frasa kitab hadis biasanya digunakan untuk membedakan dengan kata primer.
                            Jadi, ada kitab hadis primer, ada pula kitab hadis sekunder. Secara singkat kategori kitab
                            hadis primer dan sekunder dapat didefinisikan sebagai berikut. Kitab hadis 2 primer, ialah
                            kitab karya ulama (pakar hadis) yang memuat hadis-hadis Nabi. Hadis-hadis yang terdapat
                            dalam kitab primer ini adalah hasil pencarian langsung, umumnya dengan menemui para guru,
                            kemudian hadis-hadis itu diseleksi dengan mekanisme tertentu, dihimpun dan dibukukan.
                            Sedangkan kitab hadis sekunder, ialah kitab/buku karya penulis generasi sesudahnya, yang
                            memuat hadis-hadis Nabi yang diambil dari kitab hadis primer. </p>

